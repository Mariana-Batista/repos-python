#Operadores úteis
s1 = {1, 2, 3}
s2 = {2, 3, 4}

print(f'Conjuntos {s1} e {s2}')

print()
print('União')

s3 = s1 | s2

print(s3)

print()
print('Interseção')

s3 = s1 & s2

print(s3)

print()
print('Diferença')

s3 = s1 - s2

print(s3)

print()
print('Diferença simétrica')

s3 = s1 ^ s2

print(s3)

